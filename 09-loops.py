# Todos los programas o software ejecutan bloques de código en forma repetitiva hasta que cierta condición cambie. Python tiene 2 estructuras de control iterativas while y el for

# La estructura o loop While ejecuta las sentencias de su cuerpo mientras la expresión booleana sea verdadera.
# Ejemplo

num = 0
while num < 10:
    print(f"Esta es la vuelta número {num}")
    num = num + 1